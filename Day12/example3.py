a=2+3j
print(a,type(a))  # (2+3j) <class 'complex'>

b=2-5j
print(b,type(b))  # (2-5j) <class 'complex'>

c=-2+4j
print(c,type(c))  # (-2+4j) <class 'complex'>

d=2+3j
print(d,type(d))  # (2+3j) <class 'complex'>
print(a.real)   # 2.0
print(a.imag)   # 3.0

e=-2.5j
print(e,type(e))  # (-0-2.5j) <class 'complex'>

print(a.real) # 2.0
print(e.imag) # -2.5


